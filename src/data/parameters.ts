import type { KernelParameter } from "../types";

type ParameterSeed = {
  name: string;
  subsystem: string;
  valueType: string;
  exampleValue: string;
  safeRange: string;
  description: string;
  changes: string;
  affects: string[];
  relatedNodes: string[];
  relatedScenarios: string[];
  risk: string;
  path?: string;
};

const sysctlPath = (name: string): string => `/proc/sys/${name.split(".").join("/")}`;

const sysctlParameter = (seed: ParameterSeed): KernelParameter => ({
  id: seed.name,
  name: seed.name,
  subsystem: seed.subsystem,
  path: seed.path ?? sysctlPath(seed.name),
  valueType: seed.valueType,
  exampleValue: seed.exampleValue,
  safeRange: seed.safeRange,
  description: seed.description,
  changes: seed.changes,
  affects: seed.affects,
  relatedNodes: seed.relatedNodes,
  relatedScenarios: seed.relatedScenarios,
  viewCommands: [`sysctl ${seed.name}`, `cat ${seed.path ?? sysctlPath(seed.name)}`],
  changeCommands: [`sudo sysctl -w ${seed.name}=<value>`, `echo "${seed.name}=<value>" | sudo tee /etc/sysctl.d/99-local.conf`],
  risk: seed.risk,
});

const cgroupParameter = (seed: ParameterSeed): KernelParameter => ({
  id: seed.name,
  name: seed.name,
  subsystem: seed.subsystem,
  path: seed.path ?? `/sys/fs/cgroup/<path>/${seed.name}`,
  valueType: seed.valueType,
  exampleValue: seed.exampleValue,
  safeRange: seed.safeRange,
  description: seed.description,
  changes: seed.changes,
  affects: seed.affects,
  relatedNodes: seed.relatedNodes,
  relatedScenarios: seed.relatedScenarios,
  viewCommands: [`cat ${seed.path ?? `/sys/fs/cgroup/<path>/${seed.name}`}`, "systemd-cgtop"],
  changeCommands: [`echo "<value>" | sudo tee ${seed.path ?? `/sys/fs/cgroup/<path>/${seed.name}`}`],
  risk: seed.risk,
});

